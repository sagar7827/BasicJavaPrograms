package Developer;

public class VariablesAndDatatypes {

	public static void main(String[] args) {
		
	//	int a;  	// declaration
	//	a=100;		// assignment
		
		
		// Primitive Datatype only accept single value at a time.
		
		/* Primitive DataType
		 * byte , short , int ,long ----> number without decimal 
		 */
		
		int a=100; // declaration + assignment
		System.out.println(a);
		
		byte b =125;
		System.out.println(b);
		
		short sh =3535;
		System.out.println(sh);
		
		long l =12345567789L;   // literal is needed L/l
		System.out.println(l);
		
		/* Primitive Datatype
		 * float , double ---> number with decimal
		 */
		
		float item_price =15.5f;	// literal is needed F/f
		System.out.println(item_price);
		
		double d =1234.456812;
		System.out.println(d);
		
		/* primitive Datatype 
		 * char ---> only for single character ( single qoute)
		 */
		
		char grade ='A';
		System.out.println(grade);
		
		/* Primitive Datatype
		 * boolean ---> true/false
		 */
		
		boolean bl =true;
		System.out.println(bl);
		
		

	}

}
